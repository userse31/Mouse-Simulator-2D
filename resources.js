//square inverse law: intensity ∝ 1/(distance^2)
function square_inverse(x1,y1,x2,y2){
	var distance=Math.sqrt(
	Math.pow((x2-x1),2)+
	Math.pow((y2-y1),2)
	);
	return (distance<=0)?65535:Math.floor((1/(Math.pow(distance,2)))*65535);
}